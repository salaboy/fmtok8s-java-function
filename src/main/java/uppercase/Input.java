package uppercase;

import com.fasterxml.jackson.annotation.JsonClassDescription;

public class Input {

  private String input;

  public Input() {
  }

  public String getInput() {
    return input;
  }

  public void setInput(String input) {
    this.input = input;
  }

  @Override
  public String toString() {
    return "Input{" +
        "input='" + input + '\'' +
        '}';
  }
}
